struct Warp {
	virtual FVec3 warp( FVec3 p ) {
		return p;
	}
	
	virtual void draw() { 
	}
};

struct DiscWarp : Warp {
	FVec3 center;
	FVec3 normal;
	
	DiscWarp( FVec3 center, FVec3 normal ) {
		this->center = center;
		this->normal = normal;
	}
	
	virtual void draw() {
		zglDrawVector( this->center.x, this->center.y, this->center.z, this->normal.x, this->normal.y, this->normal.z );
	}
	
	virtual FVec3 warp( FVec3 p ) {
		FVec3 ap = p;
		ap.sub( this->center );
		float apMag = ap.mag();

		FVec3 pnProj = p;
		pnProj.project( this->normal );
		float pnProjMag = pnProj.mag();

		float radius = Cloud_dwa;		
		float offset = exp( Cloud_dwd * -powf((apMag-radius),Cloud_dwb) ) * exp( Cloud_dwe * -powf(pnProjMag,Cloud_dwc) );

		ap.div( apMag );
		ap.mul( apMag + offset );
		ap.add( this->center );
		return ap;
	}
};

struct Surface {
	virtual float surface( FVec3 p ) {
		return 0.f;
	}
};

struct Sphere : Surface {
	FVec3 c;
	float r;

	Sphere( FVec3 c, float r ) {
		this->c = c;
		this->r = r;
	}
	
	virtual float surface( FVec3 p ) {
		FVec3 d = p;
		d.sub( this->c );
		float m = d.mag();
		return exp(-Cloud_c*m*m) - this->r;
	}
};

struct Rod : Surface {
	FVec3 a;
	FVec3 b;
	float r;

	Rod( FVec3 a, FVec3 b, float r ) {
		this->a = a;
		this->b = b;
		this->r = r;
	}
	
	virtual float surface( FVec3 p ) {
		FVec3 ba = b;
		ba.sub( a );
		float baMag2 = ba.mag2();
		FVec3 pa = p;
		pa.sub( a );
		
		float t = max( 0.f, min( 1.f, pa.dot(ba) / baMag2 ) );
		FVec3 _proj = ba;
		_proj.mul( t );
		FVec3 proj = _proj;
		proj.add( a );
		
		FVec3 residual = proj;
		residual.sub( p );
		float residualMag = residual.mag();
		return exp(-Cloud_b* residualMag*residualMag) - this->r;
	}
};

